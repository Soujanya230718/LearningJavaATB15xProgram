package VariablesDatatypes;

public class Operators {
    public static void main(String[] args){
        int a=10;
        int b=20;
        float c= 3.5f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(a/c);
        System.out.println(a-c);
        System.out.println(a%c);

    }
}
