package IncrementDecrement;

public class Pro1 {
    public static void main(String[] args) {
        int a= 10;
        int b= ++a;
    System.out.println(b);
    System.out.println(a);
    }
}


// ++a ->  a= a+1
//  Exp and Result Table
// Line No | a | Result b
//  5     |  10 | NA |
//  6     |  11 | 11 |
// 8      |  11 - a
// 7      |  11 | 11 - print